/* you are given a string that may have mixed uppercase and lowercase letters -
your task is to convert that string to either lowercase only or uppercase only */

let fixStringCase = (string) => {

  for (let i = 0; i < string.length; i++) {
    if (this.valueOf().toUpperCase() === this.valueOf()) {
      console.log('this is uppercase');
      console.log(this);
    }
  }

}

